package Matrices;

import Modele.Point;

public class Matrice {
	private double xA, xB, xC,
				   yA, yB, yC,
				   zA, zB, Zc;
	
	public Matrice(Point p) {
		
	}

	public double getxA() {
		return xA;
	}

	public double getxB() {
		return xB;
	}

	public double getxC() {
		return xC;
	}

	public double getyA() {
		return yA;
	}

	public double getyB() {
		return yB;
	}

	public double getyC() {
		return yC;
	}

	public double getzA() {
		return zA;
	}

	public double getzB() {
		return zB;
	}

	public double getZc() {
		return Zc;
	}

}
