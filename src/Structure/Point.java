package Structure;

public class Point {
	int x,y,z;
	
	public Point(int c, int d, int e) {
		x = c;
		y = d;
		z = e;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}
	
	

}
