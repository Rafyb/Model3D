package Structure;

public class Segment {
		Point x, y;
		
		public Segment(Point c, Point d) {
			x = c;
			y = d;
		}

		public Point getX() {
			return x;
		}

		public void setX(Point x) {
			this.x = x;
		}

		public Point getY() {
			return y;
		}

		public void setY(Point y) {
			this.y = y;
		}
		
		

}
